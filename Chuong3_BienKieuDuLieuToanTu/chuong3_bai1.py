print("Chương trình tính chu vi và diện tích của hình chữ nhật.")
a = float(input("Nhập chiều dài hình chữ nhật: ")) # a là chiều dài hình chữ nhật
b= float(input("Nhập chiều rộng hình chữ nhật: ")) # b là chiều rộng hình chữ nhật

P = (a + b) * 2 # P là công thức tính chu vi hình chữ nhật
S = a * b # S là công thức tính diện tích hình chữ nhật

print(f"Chu vi hình chữ nhật: {P}")
print(f"Diện tích hình chữ nhật: {S}")